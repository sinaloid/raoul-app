        Rencontres Cinématographiques de Sya (RECIS)
    </span>
    <p class="mt-3">
        Les RECIS sont un projet de festival qui a pour but de faire la promotion des cinémas burkinabé
        et des diasporas africaines et des afros descendants et des pays du Sahel.
    </p>

    <p class="font-weight-bold text-center">a. VISION</p>
    <p>
        Les RECIS sont un projet de festival qui a pour but de faire la promotion des cinémas burkinabé, et des
        diasporas africaines et des afro-descendant.e.s et des pays du Sahel.
        Les RECIS se veulent être un cadre de rencontre et d’échanges entre les cinéastes burkinabé, de la diaspora
        africaine et afro descendante. C'est aussi un lieu où le cinéma burkinabé sera primé. En effet, il n’y a pas
        un évènement culturel national qui fait la promotion et prime le cinéma burkinabé. Les RECIS seront cet endroit
        à Bobo- Dioulasso, où le cinéma burkinabé sera célébré.
        Par ailleurs, les RECIS ambitionnent de rapprocher davantage le cinéma burkinabé et de la diaspora africaine et
        afro descendant de son public. Dans un contexte où les salles de cinéma sont quasiment inexistantes, il est
        impératif pour la survie même du cinéma africain et burkinabé en particulier, de s'enraciner dans les quartiers
        populaires d'où ce cinéma tire sa sève vivifiante.
    </p>
    <p class="font-weight-bold text-center">b. MISSION</p>

    <p>Le concept que nous proposons est novateur à plusieurs niveaux :</p>
    <ul class="px-5">
        <li>D'abord dans son approche : donner la possibilité au cinéma Burkinabè d’aller à la rencontre de son public
            en sortant des cadres de projection traditionnels</li>
        <li>Ensuite, que le cinéma de la diaspora africaine et des afro-descendants soit rendu accessible à ce public,
        </li>
        <li>Enfin que le cinéma mette en lumière d'autres sphères de la création artistique telle que la musique.</li>
    </ul>
    <p>
        Dès la 1ère édition, nous entendons impulser aux RECIS un caractère de rencontre entre professionnel du 7ième
        Art
        et aussi un caractère de manifestation populaire avec le concept « Bobo, Ville Cinéma » avec des projections en
        plein-air dans des quartiers de la ville de Bobo-Dioulasso. Cette dimension populaire restera sans doute la
        "griffe"
        des RECIS car nous allons sortir de plus en plus le cinéma des salles, pour l'amener également dans des espaces
        ouverts
        (espaces publics, terrains de sport, arbres à palabres dans les villages…) afin de toucher le maximum de
        spectateurs.
        Durant les 4 jours (du jeudi au dimanche) que vont durer les RECIS, ce sont des événements marquants qui seront
        donc
        au rendez-vous.
    </p>
    <p>
        Les RECIS seront un cadre d'épanouissement véritable de la jeunesse Bobolaise, Burkinabè et sous-régionale que
        nous
        souhaitons voir s'approprier ce festival et l'intégrer désormais dans sa vie culturelle des villages et
        départements
        environnants la ville de Bobo-Dioulasso.
    </p>

    <p class="font-weight-bold text-center">c. OBJECTIFS ET BUTS DES RECIS</p>
    <p>
        Les objectifs des RECIS, sont entre autres :
    <ul class="px-5">
        <li>Faire la promotion des cinémas burkinabè et ceux des diasporas africaines, des afro-descendant.e.s et des
            pays du Sahel et primer le cinéma Burkinabè avec les prix dénommés les Silures </li>
        <li>Promouvoir le cinéma africain
            et de la diaspora africaine et afro-descendants dans la capitale économique du Burkina Faso,</li>
        <li>Valoriser la créativité africaine et de la diaspora africaine et afro-descendants à travers le cinéma,</li>
        <li>Rencontres des cinéastes et d’autres professionnel du 7ième Art.</li>
        <li>Favoriser une culture de l'image au sein de la jeunesse de la ville de Sya et des environs,</li>
        <li>Sensibiliser la jeunesse de Sya et des environs quant à la réappropriation de sa culture,</li>
        <li>Valoriser les potentialités artistiques et culturelles diversifiées des peuples d'Afrique dans les
            productions cinématographiques africaines</li>
        <li>Enfin, donner à voir le monde à travers l'imaginaire et la sensibilité négro-africains.</li>

    </ul>

    </p>

